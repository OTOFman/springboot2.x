package com.otof.springboot2.x.entity;

public class PostRequestParam {

    private String name;
    private String age;

    public PostRequestParam() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
